No = int(input("Enter Week No 1-7"))
if No==1:
    Print("Monday")
elif No==2:
    print("Tuesday")
elif No==3:
    print("Wednesday")
elif No==4:
    print("Thursday")
elif No==5:
    print("Friday")
elif No==6:
    print("Saturday")
elif No==7:
    print("Sunday")
else:
    print("Enter a Valid Number")